//----------------------------------------
// パレットオフセットバイナリデータ
//----------------------------------------
class CPalOfsBinData{
public:
    //------------------------
    // バイナリサイズ取得
    //------------------------
    inline static int GetBinSize( void ){
        return( 1 );
    }

protected:
    //------------------------
    // メンバー
    //------------------------
    uint8 m_nPalOfs;             // [1]: 参照先

public:
    //------------------------
    // コンストラクタ／デストラクタ
    //------------------------
    CPalOfsBinData(){}
    virtual ~CPalOfsBinData(){}

    //------------------------
    // 取得
    //------------------------
    inline BYTE getPalOfs( void ){ return( m_nPalOfs ); }

    //-----------------------------------------------------
    // 読み込み（※[pIB]は有効である前提＆値の整合性等の判断はしない）
    //-----------------------------------------------------
    inline void read( CInputBuffer* pIB ){
        m_nPalOfs = pIB->readUint8();
    }
};

