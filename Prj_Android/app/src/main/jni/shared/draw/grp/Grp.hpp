//----------------------------------------
// 描画セルグループ
//----------------------------------------
class CGrp{
protected:
	int m_nSprNum;
	CSpr* m_pArrSpr[NUM_SPR_IN_GRP];

public:
	// インスタンス作成は不可
	CGrp( void );
	virtual ~CGrp( void );

    // クリア
	void clear( void );
    
	// バイナリの適用
	bool applyBin( CGrpBinData* pBin );

    // 取得
	inline int getSprNum( void ){ return( m_nSprNum ); }
	inline CSpr** getSprArr( void ){ return( m_pArrSpr ); }
    inline CSpr* getSprAt( int at ){
        if( at >= 0 && at < m_nSprNum ){
            return( m_pArrSpr[at] );
        }
        return( NULL );
    }
};

