CNum::CNum( void ){
	clear();
}

//------------------------
// デストラクタ
//------------------------
CNum::~CNum( void ){}

//-------------------
// クリア
//-------------------
void CNum::clear( void ){
	m_eGrpId = eGRP_INVALID;
	m_nMarginX = 0;
	m_bRenderAtOnce = false;
	m_eDrawSetType = (eDRAW_SET_TYPE)0;
}

//-------------------
// バイナリの適用
//-------------------
bool CNum::applyBin( CNumBinData* pBin ){
    // 用心
    if( pBin == NULL ){
        return( false );
    }
    
	m_eGrpId = pBin->getGrp();
	m_nMarginX = pBin->getMarginX();
	m_bRenderAtOnce = pBin->isRenderAtOnce();
	m_eDrawSetType = pBin->getDrawSetType();

	return( true );
}
